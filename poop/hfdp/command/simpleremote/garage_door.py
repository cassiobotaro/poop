class GarageDoor:
    def up(self) -> None:
        print("Garage door is up")

    def down(self) -> None:
        print("Garage door is closed")

    def stop(self) -> None:
        print("Garage door is stopped")

    def light_on(self) -> None:
        print("Garage light is on")

    def light_off(self) -> None:
        print("Garage light is off")
